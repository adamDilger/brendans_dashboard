export function getDailyRandomBabyName() {
  const date = new Date()
    .toLocaleDateString("en-AU", {
      timeZone: "Australia/Hobart",
    })
    .split("/")
    .reverse()
    .join("");

  return names[Number(date) % names.length];
}
